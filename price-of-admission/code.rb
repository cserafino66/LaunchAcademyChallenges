price_adult = 12.80
price_child = 4.00

puts "Welcome to the Amusement Park! How many adults in your party?"
adult_input = gets.chomp
adult_input = adult_input.to_f
puts "And how many children?"
children_input = gets.chomp
children_input = children_input.to_f

total_cost = adult_input * price_adult + children_input * price_child

puts "Your total is $#{total_cost}. Split evenly between the adults, the cost is
$#{total_cost/adult_input} each."
